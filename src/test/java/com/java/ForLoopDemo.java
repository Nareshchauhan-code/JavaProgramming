package com.java;

public class ForLoopDemo {

    public static void main(String[] args) {
        System.out.println("**********Even Number*****************");
        for (int i = 2; i <= 100; i += 2) {

            System.out.println(i);
        }

        System.out.println("**********Odd Number*****************");

        //Odd Number
        for (int i = 1; i <= 100; i += 2) {

            System.out.println(i);
        }

    }
}
